// NFT Contract Address
const NFT_CONTRACT_ADDRESS = "0x4f6F7A6772255340374eC4A955B6CeB548BAf0c0"

export function WalletProvider({ children }: { children: ReactNode }) {
  const [address, setAddress] = useState<string | null>(null)
  const [balance, setBalance] = useState("0")
  const [isConnecting, setIsConnecting] = useState(false)
  const [isConnected, setIsConnected] = useState(false)
  const [isMinting, setIsMinting] = useState(false)
  const { toast } = useToast()

  const connectWallet = async () => {
    try {
      setIsConnecting(true)
      
      if (!window.ethereum) {
        throw new Error("Please install MetaMask")
      }

      const provider = new ethers.providers.Web3Provider(window.ethereum)
      const accounts = await provider.send("eth_requestAccounts", [])
      const signer = provider.getSigner()
      
      setAddress(accounts[0])
      setIsConnected(true)
      
      // Get initial balance
      await updateBalance(accounts[0], provider)
      
      // Listen for account changes
      window.ethereum.on("accountsChanged", async (newAccounts: string[]) => {
        if (newAccounts.length === 0) {
          disconnectWallet()
        } else {
          setAddress(newAccounts[0])
          await updateBalance(newAccounts[0], provider)
        }
      })

      toast({
        title: "Wallet Connected",
        description: "Successfully connected to MetaMask",
      })
    } catch (error) {
      console.error("Error connecting wallet:", error)
      toast({
        title: "Connection Error",
        description: error instanceof Error ? error.message : "Failed to connect wallet",
        variant: "destructive",
      })
    } finally {
      setIsConnecting(false)
    }
  }

  const disconnectWallet = () => {
    setAddress(null)
    setBalance("0")
    setIsConnected(false)
    toast({
      title: "Wallet Disconnected",
      description: "Successfully disconnected from MetaMask",
    })
  }

  const updateBalance = async (userAddress: string, provider: ethers.providers.Web3Provider) => {
    try {
      const contract = new ethers.Contract(CONTRACT_ADDRESS, CONTRACT_ABI, provider)
      const balance = await contract.balanceOf(userAddress)
      setBalance(ethers.utils.formatEther(balance))
    } catch (error) {
      console.error("Error fetching balance:", error)
    }
  }

  const rewardUser = async () => {
    if (!address) return

    try {
      const provider = new ethers.providers.Web3Provider(window.ethereum)
      const signer = provider.getSigner()
      const contract = new ethers.Contract(CONTRACT_ADDRESS, CONTRACT_ABI, signer)

      
      const rewardAmount = await contract.rewardAmount()
      
      
      const tx = await contract.rewardUser(address)
      
      
      const receipt = await tx.wait()
      
      
      if (receipt.status === 1) {
        
        await updateBalance(address, provider)

        toast({
          title: "Reward Successful",
          description: `You've earned ${ethers.utils.formatEther(rewardAmount)} CLT tokens!`,
        })
      } else {
        throw new Error("Transaction failed")
      }
    } catch (error) {
      console.error("Error rewarding user:", error)
      toast({
        title: "Reward Failed",
        description: error instanceof Error ? error.message : "Failed to process reward",
        variant: "destructive",
      })
    }
  }

  const mintBadge = async () => {
    if (!address) {
      toast({
        title: "Wallet Not Connected",
        description: "Please connect your wallet to mint the badge",
        variant: "destructive",
      })
      return
    }

    try {
      setIsMinting(true)
      const provider = new ethers.providers.Web3Provider(window.ethereum)
      const signer = provider.getSigner()
      const contract = new ethers.Contract(NFT_CONTRACT_ADDRESS, NFT_CONTRACT_ABI, signer)

      // Call claimBadge function instead of mintBadge
      const tx = await contract.claimBadge()
      
      // Show loading toast
      toast({
        title: "Minting Badge",
        description: "Please wait while we mint your badge...",
      })

      // Wait for transaction to be mined
      const receipt = await tx.wait()

      if (receipt.status === 1) {
        toast({
          title: "Badge Minted Successfully!",
          description: "Congrats! You earned the DSA Khiladi Badge!",
        })
      } else {
        throw new Error("Transaction failed")
      }
    } catch (error) {
      console.error("Error minting badge:", error)
      toast({
        title: "Minting Failed",
        description: error instanceof Error ? error.message : "Failed to mint badge",
        variant: "destructive",
      })
    } finally {
      setIsMinting(false)
    }
  }

  return (
    <WalletContext.Provider
      value={{
        address,
        balance,
        isConnecting,
        isConnected,
        connectWallet,
        disconnectWallet,
        rewardUser,
        mintBadge,
        isMinting,
      }}
    >
      {children}
    </WalletContext.Provider>
  )
}

export function useWallet() {
  const context = useContext(WalletContext)
  if (context === undefined) {
    throw new Error("useWallet must be used within a WalletProvider")
  }
  return context
} 